            <h3>Special Art Collections</h3>
            <a href="./gallery/crash/captions.php"><span class="big_link">crash</span></a>
            <p>
                A special exhibition in memory of <a href="./BillBateman">Bill Bateman</a> who died February 20, 1997 at age 49 after being hit by a car in Oakland, California, USA. The <a href="./gallery/crash/captions.php">special exhibit</a> uses captions from "<a href="http://en.wikipedia.org/wiki/Crash_%281973_novel%29">Crash</a>," the book by J. G. Ballard. Ballard wrote "Crash" in 1973 and it was recently made into a movie directed by David Cronenberg who also did Scanners, The Fly, and Naked Lunch. The movie is a study in the modern search for intensity, eschews morality and breaks the boundaries of "normal." Ballard forsees our coming "autogeddon."
            </p>
            <a href="./newsletter/newsitems/wtc/intro_wtc.php"><span class="big_link">World Trade Center Art Project</span></a>
            <p>
                Carolyn created a series of watercolors in response to the Sept. 11, 2001 terrorist attacks on New York City.
            </p>
            <a href="http://wwol.inre.asu.edu/ellingson.html" target="new"><span class="big_link">The World's Women Online</span></a>
            <p>
                In 1995 this image of <span class="italic">Formalities, I</span> was accepted for an international electronic art networking project, The World's Women Online.  It was also projected as part of an installation piece created by Artist Muriel Magenta from Arizona State University which wove the Internet images with video sequences at the 1995 UN conference in Beijing.
            </p>

