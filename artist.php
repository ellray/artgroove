            <h3>Carolyn's June 1997 interview in Feminista!</h3>
            <p>
                Carolyn was interviewed by the now-defunct webzine, <span class="bold_italic">"Feminista!"</span> in 1997, and spoke about how she became an artist in spite of all the challenges she faced coming from being a stay-at-home mother of the '60s.  The article was published with numerous images of Carolyn's art and was titled, <span class="bold">"Pick up the brush, the chalk, the clay"</span>.</p><p>
                Carolyn had this to say about the interview:  "I never considered myself a feminist <span class="italic">per se</span>, but perhaps I am after all-- read this interview and decide for yourself.  I believe every woman has at least a few feminist stories in her life repertoire. The interview conducted by Leigh Anne Jones reveals what I feel it's like to be an artist here in San Francisco, and what I thinks it takes for anyone to be an artist."
            </p><p>
                The <span class="bold_italic">Feminista!</span> website is down, but <a href="./FeministaInterview">the interview can be found here</a>.  It is several pages long, but if you're interested in Carolyn's art or personal background, it's not to be missed.
            </p>

