            <h2>About Carolyn</h2>
            <p>
                Carolyn Ellingson was a San Francisco artist known for strong abstract images and clean, decisive and exciting color.  Carolyn was born in Chicago and moved with her family as a child to Minneapolis where she eventually earned advanced degrees from The University of Minnesota. She moved to California in 1983 and maintained a studio at <a href="http://www.thepointart.com" target="new">Hunter's Point Shipyard</a>. She focused on creating non-representational oil and acrylic paintings, monotypes, pastels and watercolors.  Carolyn died of mesothelioma on April 26, 2002;  <a href="obituary.php">her obituary can be read here</a>.  Carolyn's artist bio (as she wrote it) can be <a href="artistbio.php"> read here</a>.
            </p>
