        <h3>Here's the Interview:</h3>
        <p>ME: 
        <span class="italic">Fear and Trembling - Oh NO! Will I Lose My Favorite Colors?</span>
		</p>
		<p>Many of the paints artists use contain cadmium, cobalt, lead, chromium and other substances that can cause cancer and are unsafe to use in certain ways. I believe that these paints include some of the most beautiful 
		colors we artists have available to us. Something happened in July that made me think we would soon be unable to purchase these paints here in California and I was devastated. Cadmium colors are my favorite colors 
		and form the very basis of my palette. I am known for my cadmium red prints and paintings. I also love cobalt blue and chrome green.
		</p>
		<p><span class="italic">Here's What Happened to Freak Me Out</span>
		</p>
		<p>I was told by someone working in an art supply store that these paints would very soon be unavailable in California and that it would be illegal to order them from out of state or even use them here .Thankfully this 
		turned out to be misinformation or a misunderstanding-but it led me to research this issue. What I learned follows.
		</p>
		<p><span class="italic">Proposition 65 - </span>ME:<span class="italic"> I've Heard of It</span>
		</p>
        <p>
		MONONA ROSSOL continues:
		</p>
		<p>California's Proposition 65 mandates that the above-mentioned paints (and other paints such as automotive paints, for example, and many other products as well) must now be labeled to inform consumers of the dangers involved with 
		using them. All paint companies that sell artists's paints in California must now provide the proper labeling or their paints will be removed from the stores. The deadline for compliance is approaching. Prop 65 doesn't ban stuff. It just puts warning labels on products.
		</p>
		<p><span class="italic">More on Prop 65: It's a Great Law for Consumers - Did YOU Know This About Prop 65?</span>
		</p>
		<p>MONONA ROSSOL <span class="italic">says this of California's Proposition 65</span>:
		</p>
		<p>The art materials labeling law is a federal consumer law and enforced by the US Consumer Product Safety Commission. Prop 65 is a California State consumer law enforced by a combination of your State Attorney General    and those citizen enforcement groups. In fact your law allows you as an individual to bring suit against a noncompliant company--even if you have no damages to recover. It's a wonderful law. Since governments do not have to money to effectively enforce the regulations, this 'citizen enforcement clause' in your law allows any Californian to enforce. And if it goes to court or settlement, the citizen also gets a substantial part of the money from the lawsuit! This provides a motive for citizen groups to form and catch lawbreakers.
		</p>
		<p>ME: <span class="italic">Now isn't this just amazing about consumer enforcement? Did you know this? I didn't.</span>
		</p>
		<p><span class="italic">Will We Still Be Able to Buy These Paints?</span>
		</p>
		<p>So will we artists continue to be able to buy our beautiful (but potentially dangerous to health) colors? The situation at present seems to be this:
		</p>
		<p>I'm told that Golden and Gamblin have labeled their paints correctly. Robert Gamblin says all his paints are labeled correctly and Monona Rossol tells me Golden paints are also labeling their paints correctly. I don't have information for other domestic paint companies. It looks like we will be able to get a great supply of these brands-no problem. There may be other companies that are labeling correctly, but these are the only two we investigated.
		</p>
		<p><span class="italic">How About Imported Paints (from Europe?)</span>
		</p>
		<p>MONONA ROSSOL: Paints imported from Europe lacking proper Prop 65 labels, along with any other paints that aren't correctly labeled, will disappear from the shelves of the paint stores until and unless they provide the proper labeling for California. This means that Schminke, Le Franc and Bougeois, Winsor Newton, Old Holland, Holbein and any other imported paints containing the aforementioned hazardous substances may be unavailable for awhile-time will tell how these companies respond to the legislation requiring warning labeling in California.
		</p>
		<p>ME: This is a bit unnerving for me as I go to many of these brands to get certain colors I like. Also, many of these imported paints work better for monotypes for me than the domestic brands-they seem to be more loaded with pigment and cover better.
		</p>
		<p><span class="italic">Here's Something Else about Paints from Europe</span>
		</p>
		<p>MONONA ROSSOL wrote to me:
		</p>
		<p>Since 1991, Europe has had a ban on cadmium in many applications including paints. I have a copy of the Information Brochure on the European Cadmium Legislation, and it doesn't seem to exempt art paints. But it also doesn't mention them specifically.
		</p>
		<p>You might want to contact Schminke or Winsor &amp; Newton here in the US and ask if the European Cadmium Legislation applies to art paints.
		</p>
		<p>But whether the European legislation applies or not, it does not stop them from manufacturing cadmium paints for export. So they can make and sell them here if they wish.
		</p>
		<p>
		<span class="italic">What's the Problem Here Anyway? Why isn't Everything Labeled Already? This Law Has Been Around Awhile.</span>
		</p>
		<p>MONONA ROSSOL says:
		</p>
		<p>The real problem is that ACMI toxicologists have told all the art materials companies for years that they don't have to put Prop 65 warnings on their products if they label to ASTM D-4236. And (as I predicted) it looks like that is not true. And the Citizens enforcement groups have the right to sue paint companies for selling in California in violation of their laws.
		</p>
		<p>But the citizens enforcement groups are going after all the other companies that don't label to Prop 65. Some of these are even more outrageous. Some companies are still taking advantage of the ASTM D 4236 rules which allow toxicologists to certify products as &quot;nontoxic&quot; that contain cadmium by claiming that when used as directed, the artists wont be exposed. Well....we know artists. They don't follow directions. They need to know that cadmium is there before they start to experiment! I say, hooray for the Prop 65 citizens enforcement groups. 
		</p>
		<p>Obviously, these groups cannot sue the companies under Prop 65 unless they are breaking that law. And that law bans nothing. It only provides consumer warnings.
		</p>
		<p>Now I admit that changing all your paint labels to add another warning can be costly. But probably cheaper than defending a losing lawsuit. And the companies who want to recover some of that money might consult their attorneys about holding liable the ACMI and other certifiers who gave them the rotten advice to sell in California while not in compliance with California's laws!
		</p>
		<p>You, as an artist, have no serious problems here. There may be a period of time during which art materials manufacturers don't sell in California because they haven't fixed their labels. But across state lines the paints will all be there. And as soon as the companies add the required warnings, all will be back to normal.
		</p>
		<p>If I were a Californian, I would be damn proud that most products--not just art materials--in this country that contain toxic chemicals on California's list carry those special warnings. On some products, these are the only warnings people get! That is one reason why Golden's testimony used the word &quot;confusing&quot; with regard to Prop 65 labeling. There literally are products on the market which give no indication whatever of any toxic ingredients and then at the very bottom it will say that the product contains substances &quot;known to the State of California&quot; to be carcinogens, reproductive hazards, or whatever. Damn right this is confusing. But without the California label, the user would have no clue at all.
		</p>
		<p><span class="italic">How About Safer Alternatives to the Cadmiums and Other Dangerous Paints?</span>
		</p>
		<p>I was so relieved to learn that my favorite colors won't be banned and that Prop 65 does not ban products but only requires warning labels.
		</p>
		<p>MONONA ROSSOL SAYS:
		</p>
		<p>The only time I would support a ban is if/when there is a safer substitute that has all the attributes of cadmium. And that may just happen with the development of the new cerium and other rare earth pigments. There may even be better pigments than cadmium in the works.
		</p>
		<p>After all, you only want something that will work and look like cadmium. If it actually is cadmium is not relevant. That's why I have to laugh when watercolorists complain about cadmium restrictions. In many cases, their &quot;cadmium&quot; paints contain no cadmium! They just look like cadmium. 
		</p>
		<p>But I do not support the use of cadmium in ordinary printing matter such as magazines and newspapers. This adds tons of cadmium to land fills for no important use.
		</p>
		<p>And I also do not support the use of cadmium in the way schools teach with them. They need to explain the hazards and the environmental problems with their use as part of the curriculum. And cadmium paints never should be used by children.
		</p>
		<p>And TESIA BLACKBURN, Hunters Point Shipyard artist, who is also a new employee of Golden Paints, gives me this information (she's busy studying for her new position!):
		</p>
		<p>I don't know about the oil paints but Golden is continuously doing research and trying to get better pigments to replace cadmium. In fact, the Pyrrole red that they have is MORE LIGHTFAST and richer in color than the original Cadmium Red. Cadmium red is not all that lightfast compared to this Pyrrole red. It's very yummy indeed. Also the Hansa yellow that they make is absolutely gorgeous as is the Diarylide yellow.
		</p>
		<p>Of course this is all acrylic, I don't have a clue what's going on with oil. However, Golden has phased out all of their old labeling and now only has labels that conform to Prop 65 so as I understand it, their colors will not be taken off the shelves.
		</p>
		<p>Also, it may interest you to know that only 2-4 percent of the pigments that are produced are used in artists' paint. The automobile industry uses the lion's share of these pigments and you can bet they pollute a lot more than we artists do!
		</p>
		<h3>CALIFORNIA HAS DONE A &quot;GRAND THING&quot;</h3>
		<p>MONONA ROSSOL said in October (10/8/01):
		</p>
		<p>I thought you should know that two companies have been sued under the citizens enforcement for Prop 65 and now it looks like they all will be labeling. Expect to see Prop 65 lead warnings even on Titanium White.
		</p>
		<p>This is such a good thing. It is important for artists to know when a pigment contains even small amounts of the highly toxic substances such as lead and cadmium since they are likely to experiment with the products.
		</p>
		<p>The companies always claim they will be broke and the consumers will not buy their products. It never happens.
		</p>
		<p>California has again done a grand thing that has nationwide implications.
