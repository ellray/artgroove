      <h2>WHAT IS A GICLEE PRINT?</h2>
      <h3>by Carolyn Ellingson</h3>
      <p>A giclee print is a print produced on an ink jet printer with archival, lab tested inks that coat the paper or canvas with pigment. Giclees are popularly known as &quot;Iris&quot; prints, but technically only prints printed on Iris brand printers are Iris prints. My pink image was printed on an actual Iris printer by Redwood Editions</p>
      <p>High resolution is possible, as much as 1800 dpi.</p>
      <p>Giclee fine art prints are usually produced by scanning in the image from a 4x5 or larger transparency, after which they are printed digitally.</p>
      <p>One testing entity has found these prints to perfectly retain their color fastness from 25-200 years depending on materials used and the conditions under which they exist.</p>
      <p>Giclees should be treated much as watercolors are treated and cared for. They can fade if kept in bright light. They may be destroyed by water (though I'm reading that this is no longer true). Also their lightfastness has improved greatly in the past few years.</p>
      <p>Giclees can be printed on demand (the whole edition does not need to be printed at once). The image can be tweaked to be even better than the original image. The visual quality is extremely high, the color reproduction very true to the original.</p>
      <p>They are fairly expensive to produce because of the cost of inks, paper, specialist time, length of time the machine requires to produce the print (maybe an hour to produce one sheet). The machines can cost up to $25,000 and the paper can be expensive as well.</p>
      <p>My giclees have been printed with archival inks and papers.</p>
      <p>Giclees are less expensive than originals and look very, very good. It's a way to make a popular image available to more than one person at a price more can afford.</p>
