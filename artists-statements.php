			<a href="#" onclick="toggle_faq_display('1999'); return(false);"><h3>Artist's Statement Feb 1999</h3></a>
			</p>
			<p id="1999" style="display:none">
			I create art to introduce into the environment something new and visually interesting.  Also I create art in order to have something really exciting and satisfying to do.  Being an artist has enormous appeal for me.  I feel I have chosen the best thing imaginable to do and to be.
			<br /><br />
			The <span class="underline">process</span> of creating images is as satisfying to me as the <span class="underline">outcome</span>.  The fact that these images materialize where before there was nothing, the fact that they come from somewhere &quot;inside&quot; of me and the supply is not limited except by whatever limits are imposed by my circumstances, the fact that I don't plan them very much and don't <span class="underline">myself</span> know what to expect to have happen when I'm working--these things make doing art seem a lot like magic.
			<br /><br />
			Of course it is <span class="underline">hard work</span> to make a piece of art come together so that it really works, without editing out all the irregularities and disturbances that make the image interesting.  This successful resolution is one of the most important parts of the process that makes the image <span class="underline">art</span> rather than just an expressive gesture.  Sometimes I find <span class="underline">this</span> part of the process to be not as easy or enjoyable as the other.  Not everything that emerges is allowed to <span class="underline">live</span>!  But more and more often I am able to achieve that successful outcome.
			<br /><br />
			My images tend to be non-figurative, non-representational, and always, I hope, contemporary, for that is the kind of art I love.  I frequently use intense colors and I believe that color is my forte.  As for the question often asked (especially with regard to non-representational art), &quot;What does it mean?&quot;  I would probably answer, &quot;My art communicates the ineffable--I cannot say in words what it means&quot;.
			<br /><br />
			<span class="bold">February 1999</span>
			</p> 

			<p>
			<a href="#" onclick="toggle_faq_display('1992'); return(false);"><h3>November 1992 (for Shipyard Printmakers Guild Show)</h3></a>
			</p>
			<p id="1992" style="display:none">
			My painterly monotypes come into existence spontaneously and intuitively as I begin the process of painting on the plate.  Color is of enormous importance to me.  I usually work with oil paints, both because of their ease of manipulation and their high color quality.  I have researched the use of oil paints for monotypes and can provide a copy of the research to those who are interested.  I use collage with Japanese handmade papers and metallic leaf on some of my prints.  sometimes I also paint on the finished print.
			<br /><br />
			Like paintings, these prints are truly one of a kind--I never do a ghost print.  What then is the point of doing a monotype rather than a painting?  The monotype has a different look than a painting.  The process is more unruly and difficult:  the paint application to the plate must be &quot;clean&quot; (overpainting on the plate is usually not feasible for the effects I am trying to achieve), the paint will be offset, the surface perfectly flat to the picture plane, the image reversed, and the opportunity for revision near zero.  The artist is collaborating with the press, which will add an element of surprise to the artist's intended results.  Because of these variables, I find monotype to be a sophisticated, challenging medium with a definite look and identity of its own.
			<br /><br />
			<span class="bold">November 1992, Shipyard Printmakers Guild</span>
			</p>
			
